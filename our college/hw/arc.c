//wap in c to find area and perimeter of circle for given radius 
#include<stdio.h>
int main()
{
    int arc,pr,r;
    r=5;
    arc= 3.14*r*r;
    pr=2*3.14*r;
    printf("Enter the area :- %d\n , Enter the perimeter :- %d",arc,pr);
    return 0;
}
